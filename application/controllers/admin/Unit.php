	function hapus_unit(){
		$kode=$this->input->post('kode');
		$gambar=$this->input->post('gambar');
		$path='./assets/images/'.$gambar;
		unlink($path);
		$this->m_unit->hapus_unit($kode);
		echo $this->session->set_flashdata('msg','success-hapus');
		redirect('admin/unit');
	}

}